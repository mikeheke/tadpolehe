public class SmsTemplateVo {
	
    //自动生成ID，主键
    private String smsTemplateId;
    
    private String[] smsTemplateIds;
    
    //模板编号
    private String templateCode;
    
    //短信模板内容
    private String smsTemplateContent;
    
    //应用代码
    private String applicationId;
    
    //使用状态
    private String state;
    
    //备注
    private String remark;

	public String getSmsTemplateId() {
		return smsTemplateId;
	}

	public void setSmsTemplateId(String smsTemplateId) {
		this.smsTemplateId = smsTemplateId;
	}

	public String[] getSmsTemplateIds() {
		return smsTemplateIds;
	}

	public void setSmsTemplateIds(String[] smsTemplateIds) {
		this.smsTemplateIds = smsTemplateIds;
	}

	public String getTemplateCode() {
		return templateCode;
	}

	public void setTemplateCode(String templateCode) {
		this.templateCode = templateCode;
	}

	public String getSmsTemplateContent() {
		return smsTemplateContent;
	}

	public void setSmsTemplateContent(String smsTemplateContent) {
		this.smsTemplateContent = smsTemplateContent;
	}

	public String getApplicationId() {
		return applicationId;
	}

	public void setApplicationId(String applicationId) {
		this.applicationId = applicationId;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
 
}
