public class LogVo {

	private String applicationId;
	
	private String applicationName;
	
	private String fixWay;
	
	private String logLevel;
	
	private String logFileAdr;

	public String getApplicationId() {
		return applicationId;
	}

	public void setApplicationId(String applicationId) {
		this.applicationId = applicationId;
	}

	public String getApplicationName() {
		return applicationName;
	}

	public void setApplicationName(String applicationName) {
		this.applicationName = applicationName;
	}

	public String getFixWay() {
		return fixWay;
	}

	public void setFixWay(String fixWay) {
		this.fixWay = fixWay;
	}

	public String getLogLevel() {
		return logLevel;
	}

	public void setLogLevel(String logLevel) {
		this.logLevel = logLevel;
	}

	public String getLogFileAdr() {
		return logFileAdr;
	}

	public void setLogFileAdr(String logFileAdr) {
		this.logFileAdr = logFileAdr;
	}

}
